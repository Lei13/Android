package Constance;

public class MusicInfoBean {
	private String musicName;
	private String musicArtist;
	private String nusicLyric;
	private int musicImage;
	private String musicPath;
	
	public String getMusicPath() {
		return musicPath;
	}
	public void setMusicPath(String musicPath) {
		this.musicPath = musicPath;
	}
	public String getMusicName() {
		return musicName;
	}
	public void setMusicName(String musicName) {
		this.musicName = musicName;
	}
	public String getMusicArtist() {
		return musicArtist;
	}
	public void setMusicArtist(String musicArtist) {
		this.musicArtist = musicArtist;
	}
	public String getNusicLyric() {
		return nusicLyric;
	}
	public void setNusicLyric(String nusicLyric) {
		this.nusicLyric = nusicLyric;
	}
	public int getMusicImage() {
		return musicImage;
	}
	public void setMusicImage(int musicImage) {
		this.musicImage = musicImage;
	}


}
